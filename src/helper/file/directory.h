#pragma once

#include <execinfo.h>
#include <sys/stat.h>

#include <string>

using namespace std;

static bool
DirExists(const std::string& path)
{
    struct stat info;
    if (stat(path.c_str(), &info) != 0)
    {
        return false;
    }
    return (info.st_mode & S_IFDIR) != 0;
}

static bool
MakeDir(const std::string& path)
{
    mode_t mode = 0755;
    int ret = mkdir(path.c_str(), mode);
    if (ret == 0)
        return true;

    switch (errno)
    {
        case ENOENT: // no parents
        {
            size_t pos = path.find_last_of('/');
            if (pos == std::string::npos)
                return false;
            if (!MakeDir(path.substr(0, pos)))
                return false;
        }
            return 0 == mkdir(path.c_str(), mode);
        case EEXIST:
            return DirExists(path);

        default:
            return false;
    }
}
